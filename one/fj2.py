from turtle import * 

setup(650,650)
pensize(50)
speed(10)
hideturtle()
penup()
goto(-300,0)
pendown()
pencolor("red")
seth(90)
circle(-300,360)
penup()
goto(-200,0)
pendown()
color("red","blue")
seth(90)
begin_fill()
circle(-200,360)
end_fill()
pensize(5)
penup()
goto(0,0)
seth(162)
fd(175)
seth(0)
pendown()
fillcolor("white")
pencolor("white")
begin_fill()
for i in range(5):
    fd(330)
    right(144)
end_fill()

done()